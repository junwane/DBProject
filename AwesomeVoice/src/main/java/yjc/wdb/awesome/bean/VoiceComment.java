package yjc.wdb.awesome.bean;

public class VoiceComment {
	
	private String vc_send;
	private String vc_accept;
	private int vc_point;
	private String vc_comment;
	public String getVc_send() {
		return vc_send;
	}
	public void setVc_send(String vc_send) {
		this.vc_send = vc_send;
	}
	public String getVc_accept() {
		return vc_accept;
	}
	public void setVc_accept(String vc_accept) {
		this.vc_accept = vc_accept;
	}
	public int getVc_point() {
		return vc_point;
	}
	public void setVc_point(int vc_point) {
		this.vc_point = vc_point;
	}
	public String getVc_comment() {
		return vc_comment;
	}
	public void setVc_comment(String vc_comment) {
		this.vc_comment = vc_comment;
	}

}
