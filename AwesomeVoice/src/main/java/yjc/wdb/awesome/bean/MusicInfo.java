package yjc.wdb.awesome.bean;

public class MusicInfo {
	
	private int mi_no;
	private String mi_artist;
	private String mi_title;
	private String mi_content;
	private String mi_img;
	
	public int getMi_no() {
		return mi_no;
	}
	public void setMi_no(int mi_no) {
		this.mi_no = mi_no;
	}
	public String getMi_artist() {
		return mi_artist;
	}
	public void setMi_artist(String mi_artist) {
		this.mi_artist = mi_artist;
	}
	public String getMi_title() {
		return mi_title;
	}
	public void setMi_title(String mi_title) {
		this.mi_title = mi_title;
	}
	public String getMi_content() {
		return mi_content;
	}
	public void setMi_content(String mi_content) {
		this.mi_content = mi_content;
	}
	public String getMi_img() {
		return mi_img;
	}
	public void setMi_img(String mi_img) {
		this.mi_img = mi_img;
	}

}
